CREATE TABLE BALANCE(
    USERID BIGINT ,
    USERBALANCE REAL,
    UNIQUE (USERID)
);

CREATE TABLE RESERVED(
    USERID BIGINT ,
    SERVICEID BIGINT ,
    ORDERID BIGINT,
    SUM REAL,
    UNIQUE (USERID, SERVICEID, ORDERID)
);

CREATE TABLE FINISHED(
    USERID BIGINT ,
    SERVICEID BIGINT ,
    ORDERID BIGINT,
    SUM REAL,
    UNIQUE (USERID, SERVICEID, ORDERID)
);